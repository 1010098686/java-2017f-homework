import java.util.*;

public class CalabashBrother{
    private String name;
    private String color;
    private int no;

    public CalabashBrother(String name,String color,int no){
        this.name = name;
        this.color = color;
        this.no = no;

    }

    public String getName() {
        return this.name;
    }

    public String getColor() {
        return this.color;
    }

    public int getNo(){
        return this.no;
    }


}
